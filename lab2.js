function sum(a,b){
    return a + b;
}
function sub(a,b){
    return a - b
}
function multiply(a,b){
    return a * b
}
function divide (a,b){
    return a / b
}
console.log(sum(10,20))
console.log(sub(50,23))
console.log(multiply(13,17))
console.log(divide(202,4));
